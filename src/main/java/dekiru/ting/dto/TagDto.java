package dekiru.ting.dto;

/**
 * The data transfer object that represents a tag.
 */
public class TagDto {
    private Long id;

    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
